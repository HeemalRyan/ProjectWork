package org.Heemal.Ryan;

public class PaintProperties {
	
	private String Colour;
	private String Type;
	
	//Getters and Setters
	public String getColour() {
		return Colour;
	}
	public void setColour(String colour) {
		Colour = colour;
	}
	public String getType() {
		return Type;
	}
	public void setType(String type) {
		Type = type;
	}

}
